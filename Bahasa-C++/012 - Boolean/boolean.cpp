#include <iostream>
using namespace std;

int main(){

    // BOOLEAN 
    // 1 = True Atau Benar
    // 0 = False Atau Salah 

    // Contoh Boolean Values
    bool benar = true;
    bool salah = false;
    cout << "Ini Benar : " << benar << endl;
    cout << "Ini Salah : " << salah << endl;

    // Contoh Boolean Expresions
    int X = 12, Y = 2;

    cout << ( X == Y ) << endl; // 0 Berarti Salah
    cout << ( X > Y ) << endl; // 1 berarti Benar
    cout << ( X < Y ) << endl; // 0 Berarti Salah
}