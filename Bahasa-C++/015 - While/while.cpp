#include <iostream>
using namespace std;

int main(){

    // Buat Value X
    int X = 0;

    // Pengulangan Dengan While

    // Buat Kondisi Dulu
    while( X < 5 ){ // ini kondisi dimana jika X true maka
    // akan diulang

        cout << X << ", "; // outputnya
        X++; // increment untuk menambah value X
    }
    cout << endl;
}