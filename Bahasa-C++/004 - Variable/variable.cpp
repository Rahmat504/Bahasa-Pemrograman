// INT, FLOAT, CHAR, STRING, BOOLEAN
#include <iostream>
using namespace std;

int main(){

    // INT ADALAH BILANGAN DESIMAL
    int ANGKA = 12;
    // FLOAT ADALAH BILANG REAL
    float REAL = 33.5;
    // CHAR HANYA BISA SATU KARAKTER ABJAD
    char HURUF = 'A';
    // STRING BISA LEBIH DARI 1 KARAKTER
    string KALIMAT = "SEMOGA SEHAT SELALU !!!";
    // BOOL / BOLEAN ADALAH PILIHAN TRUE ATAU FALSE
    bool BENAR = true; // TRUE NILAINYA 1
    bool SALAH = false; // FALSE NILAINYA 0

    // OUTPUT
    cout << ANGKA <<endl;
    cout << REAL <<endl;
    cout << HURUF <<endl;
    cout << KALIMAT <<endl;
    cout << BENAR <<endl;
    cout << SALAH <<endl;
}