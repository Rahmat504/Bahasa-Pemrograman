// ==, != , > , < , >= , <=
#include <iostream>
using namespace std;

int main(){

    // BUAT 2 VALUE
    int x = 6, y = 2;

    // == ATAU SAMA DENGAN
    cout << "( x == y ) = " << ( x == y ) << endl;
    // != ATAU TIDAK SAMA DENGAN
    cout << "( x != y ) = " << ( x != y ) << endl;
    // > ATAU LEBIH BESAR DARI
    cout << "( x > y ) = " << ( x > y ) << endl;
    // < ATAU LEBIH KECIL DARI
    cout << "( x > y ) = " << ( x > y ) << endl;
    // >= ATAU LEBIH DARI SAMA DENGAN
    cout << "( x >= y ) = " << ( x >= y ) << endl;
    // <= ATAU KURANG DARI SAMA DENGAN
    cout << "( x <= y ) = " << ( x <= y ) << endl;

    // 1 = TRUE DAN 0 = FALSE
}